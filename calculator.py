def add(a, b):
    """Adds two numbers and returns the result."""
    return a + b

def subtract(a, b):
    """Subtracts b from a and returns the result."""
    return a - b

def multiply(a, b):
    """Multiplies two numbers."""
    return a * b
def divide(a,b):
    """Divide two numbers."""
    return a/b
def cube(n):
    """Returns the cube of a number."""
    return n ** 3
